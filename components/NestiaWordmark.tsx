export default function NestiaWordmark({
  logoColor = "currentColor",
  autoPlay = true,
  duration = 2.4,
  className,
  style,
  ...svgProps
}: NestiaWordmarkProps) {
  const svgRef = useRef<SVGSVGElement>(null);
  const wipeRectRef = useRef<SVGRectElement>(null);

  // useId is stable across SSR/client hydration. Remove ":" for simple SVG URL ids.
  const instanceId = useId().replace(/:/g, "");
  const wipeClipId = `nestia-wordmark-wipe-${instanceId}`;

  useLayoutEffect(() => {
    const svg = svgRef.current;
    const wipeRect = wipeRectRef.current;

    if (!svg || !wipeRect) {
      return;
    }

    const reducedMotion = window.matchMedia(
      "(prefers-reduced-motion: reduce)",
    ).matches;

    const context = gsap.context(() => {
      if (reducedMotion || !autoPlay) {
        gsap.set(svg, { autoAlpha: 1 });
        gsap.set(wipeRect, { attr: { width: VIEWBOX_WIDTH } });
        return;
      }

      gsap.set(svg, { autoAlpha: 1 });
      gsap.set(wipeRect, { attr: { width: 0 } });
      gsap.to(wipeRect, {
        attr: { width: VIEWBOX_WIDTH },
        duration,
        ease: "power1.inOut",
      });
    }, svg);

    return () => context.revert();
  }, [autoPlay, duration]);

  return (
    <svg
      ref={svgRef}
      viewBox={`0 0 ${VIEWBOX_WIDTH} ${VIEWBOX_HEIGHT}`}
      role="img"
      aria-label="nestia."
      preserveAspectRatio="xMidYMid meet"
      className={className}
      style={{
        color: logoColor,
        display: "block",
        visibility: "hidden",
        ...style,
      }}
      {...svgProps}
    >
      <defs>
        <clipPath id={wipeClipId} clipPathUnits="userSpaceOnUse">
          {/* Left-to-right wipe: width animates 0 -> full viewBox width. */}
          <rect
            ref={wipeRectRef}
            x="0"
            y="0"
            width={VIEWBOX_WIDTH}
            height={VIEWBOX_HEIGHT}
          />
        </clipPath>
      </defs>

      <g
        fill="currentColor"
        fillRule="evenodd"
        clipPath={`url(#${wipeClipId})`}
      >
        {WORDMARK_PATHS.map((d, i) => (
          <path key={i} d={d} />
        ))}
      </g>
    </svg>
  );
}
